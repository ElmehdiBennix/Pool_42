#include <unistd.h>

void	ft_putchar(char a)
{
	write (1, &a, 1);
}

int	main(int ac, char *av[])
{
	int	i;
	int	j;

	j = 1;
	while (j < ac)
	{
		i = 0;
		while (av[j][i] != '\0')
		{
			ft_putchar(av[j][i]);
			i++;
		}
		ft_putchar('\n');
		j++;
	}
	return (0);
}
